package com.personal.agenda;

public class MensajeForm {
    private String dropdownDia;
    private String dropdownMes;
    private String dropdownAnio;
    private String dropdownHora;
    private String dropdownMinuto;
    private String asunto;

    public String getDropdownDia() {
        return dropdownDia;
    }

    public void setDropdownDia(String dropdownDia) {
        this.dropdownDia = dropdownDia;
    }

    public String getDropdownMes() {
        return dropdownMes;
    }

    public void setDropdownMes(String dropdownMes) {
        this.dropdownMes = dropdownMes;
    }

    public String getDropdownAnio() {
        return dropdownAnio;
    }

    public void setDropdownAnio(String dropdownAnio) {
        this.dropdownAnio = dropdownAnio;
    }

    public String getDropdownHora() {
        return dropdownHora;
    }

    public void setDropdownHora(String dropdownHora) {
        this.dropdownHora = dropdownHora;
    }

    public String getDropdownMinuto() {
        return dropdownMinuto;
    }

    public void setDropdownMinuto(String dropdownMinuto) {
        this.dropdownMinuto = dropdownMinuto;
    }

    public String getAsunto() {
        return asunto;
    }

    public void setAsunto(String asunto) {
        this.asunto = asunto;
    }
}
